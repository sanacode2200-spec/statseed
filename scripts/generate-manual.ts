/**
 * StatSeed 完全マニュアル Markdown 自動生成スクリプト
 *
 * capture-full-manual.ts が撮影した screenshots/manual/ のスクショを取り込み、
 * docs/manual/00-getting-started.md … 12-export.md を生成する。
 *
 * 各検定ページの構成:
 *   - この検定はいつ使うか（コメディカル向けのやさしい説明）
 *   - スクリーンショット付きステップバイステップ手順
 *   - 結果の読み方
 *   - よくあるつまずきポイント
 *
 * 実行:
 *   node scripts/generate-manual.ts
 */

const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.resolve(__dirname, "..");
const SHOTS = path.join(ROOT, "screenshots", "manual");
const DOCS = path.join(ROOT, "docs", "manual");
// docs/manual/*.md から screenshots/manual/ への相対パス
const REL = "../../screenshots/manual";

const missing: string[] = [];

/** スクショが存在すれば Markdown 画像、無ければ控えめな注記を返す */
function img(cat: string, file: string, alt: string): string {
  const abs = path.join(SHOTS, cat, file);
  if (fs.existsSync(abs)) {
    return `![${alt}](${REL}/${cat}/${file})`;
  }
  missing.push(`${cat}/${file}`);
  return `> （スクリーンショット \`${cat}/${file}\` は未生成です。\`scripts/capture-full-manual.ts\` を実行してください）`;
}

function write(name: string, body: string) {
  fs.mkdirSync(DOCS, { recursive: true });
  fs.writeFileSync(path.join(DOCS, name), body.trimStart() + "\n", "utf-8");
  console.log(`  📝 docs/manual/${name}`);
}

